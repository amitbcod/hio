    public function index()
    {
        $this->check_redirect();
        // $shopcode = SHOPCODE;
        // $shop_id = SHOP_ID;

        $error_identifier = 'custom_error_pages';
        $data['ApiResponse'] = $ApiResponse =  GlobalRepository::get_custom_variable($error_identifier);
        if (isset($ApiResponse->statusCode) && $ApiResponse->statusCode == '200') {
            $error_response = $ApiResponse->custom_variable;

            if ($error_response->value == 1) {
                redirect(BASE_URL);
            } else {
                set_status_header(404);

                $identifier = '404-not-found';
                $data['ApiResponsePage'] = $ApiResponsePage =  CmsRepository::get_cms_page($identifier);
                if (isset($ApiResponsePage->statusCode) && $ApiResponsePage->statusCode == '200') {
                    $customer_type_id = $this->session->userdata('CustomerTypeID');
                    $data['customer_type_id'] = $customer_type_id = isset($customer_type_id) ? $customer_type_id : 1;

                    $data['error_page'] = $ApiResponsePage->cms_page_detail;
                    $this->template->load('common/custom_404_error', $data);
                } else {
                    $this->template->load('common/custom_404_error');
                }
            }
        }
    }

